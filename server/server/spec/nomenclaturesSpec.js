var request = require("request");
var setups = require("./setups");
var auth = require("./auth");

require("jasmine-expect");

describe("nomenclatures api", function () {
    var grant = null;
    auth(function (e, res, body) {
        grant = body;
    });

    beforeEach(function () {
        return waitsFor(function () {
            return !!grant;
        });
    });

    it("read nomenclature groups", function (done) {
        request.get({
            uri: setups.path("/api/catalogs/nomenclatures/groups"),
            headers: { "Authorization": ("Bearer " + grant.access_token) },
            json: true
        }, function (e, res, body) {
            expect(e).toBeNull();
            expect(res.statusCode).toBe(200);
            expect(body).toBeArray();
            done();
        });
    });

    it("read nomenclatures", function (done) {
        request.get({
            uri: setups.path("/api/catalogs/nomenclatures"),
            headers: { "Authorization": ("Bearer " + grant.access_token) },
            json: true
        }, function (e, res, body) {
            expect(e).toBeNull();
            expect(res.statusCode).toBe(200);
            expect(body).toBeArray();
            done();
        });
    });

    it("read nomenclatures by group (1)", function (done) {
        request.get({
            uri: setups.path("/api/catalogs/nomenclatures?group=1"),
            headers: { "Authorization": ("Bearer " + grant.access_token) },
            json: true
        }, function (e, res, body) {
            expect(e).toBeNull();
            expect(res.statusCode).toBe(200);
            expect(body).toBeArray();
            done();
        });
    });
});
